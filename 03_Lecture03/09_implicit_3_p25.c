#include <stdio.h>

int main()
{
    unsigned int u = 50;
    int i = -500;
    int answer = i / u; // answer = 85899335.
    printf("%d\n",answer);
    // i будет приведен к беззнаковому типу и его
    //значение будет равно 2^32-500 вместо -500, а результат
    //деления будет приведен обратно к знаковому типу int
}
